// Floats exercise
   
public class Floats {

  public static void main(String[] args) {
    double radius  = 2.0;
	
    double area = radius * radius * 3.14;
    radius = 1.0;
    area = radius * radius * 3.14;
    System.out.println("\n Area of a circle with radius " + 
    radius + " = " + area);
  }
}

